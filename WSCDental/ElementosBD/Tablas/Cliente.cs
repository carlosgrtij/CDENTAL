        {
            get { return emergenciaAvisar; }
            set { emergenciaAvisar = value; }
        }

        public string Medico
        {
            get { return medico; }
            set { medico = value; }
        }

        public int TipoOcupacion
        {
            get { return tipoOcupacion; }
            set { tipoOcupacion = value; }
        }

        public bool Dama
        {
            get { return dama; }
            set { dama = value; }
        }

        public DateTime FechaNacimiento
        {
            get { return fechaNacimiento; }
            set { fechaNacimiento = value; }
        }

        public string Apellido2
        {
            get { return apellido2; }
            set { apellido2 = value; }
        }


        public string Apellido1
        {
            get { return apellido1; }
            set { apellido1 = value; }
        }

        public string Nombre
        {
            get { return nombre; }
            set { nombre = value; }
        }

        public string Cedula
        {
            get { return cedula; }
            set { cedula = value; }
        }
    }
}
