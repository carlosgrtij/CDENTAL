using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ElementosBD.Tablas
{
    public class Cliente
    {
        string cedula;
        string nombre;
        string apellido1;
        string apellido2;
        DateTime fechaNacimiento;
        bool dama;
        int tipoOcupacion;
        string medico;
        string emergenciaAvisar;

