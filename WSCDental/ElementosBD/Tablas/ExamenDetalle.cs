using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ElementosBD.Tablas
{
    public class ExamenDetalle
    {
        int examen;
        int pieza;
        int tipoPosicion;
        int tipoEstadoPieza;
        string observaciones;

        public string Observaciones
        {
            get { return observaciones; }
            set { observaciones = value; }
        }

        public int TipoEstadoPieza
        {
            get { return tipoEstadoPieza; }
            set { tipoEstadoPieza = value; }
        }

        public int TipoPosicion
        {
            get { return tipoPosicion; }
            set { tipoPosicion = value; }
        }

        public int Pieza
        {
            get { return pieza; }
            set { pieza = value; }
        }

        public int Examen
        {
            get { return examen; }
            set { examen = value; }
        }

    }
}
